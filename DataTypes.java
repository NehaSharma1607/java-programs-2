public class DataTypes {
    public static void main(String[] args){
        //creating variables--comment
        int a=10;
        short b=9;
        long c= 159865326863L;  // need to add l/L to tell it is long int
        byte d=2;
        float e=1.2359f;  // need to add f to tell it is floated
        double f=85495.8541554d; //need to add d to tell it is double
        boolean g=true;
        char h='a';  // only one letter is allowed
        //Printing variables
        System.out.println("a="+a);
        System.out.println("b="+b);
        System.out.println("c="+c);
        System.out.println("d="+d);
        System.out.println("e="+e);
        System.out.println("f="+f);
        System.out.println("g="+g);
        System.out.println("h="+h);

    }
}
