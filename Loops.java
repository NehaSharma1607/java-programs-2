public class Loops {
    public static void main(String[] args){
        //Get squares of first 5 integers

        //Using for loops
        //for(<initialization>;<continuation test>;<update>){
        //     the code that is repeated
        //}
        for (int i=1 ; i<=5;i++){
            System.out.println( i +" squared = "+ (i * i) );
        }
    }
}
